package com.homework2;

public class TimeTable9 {

    public static void main(String[] args) {

        // 9 times table
        int number = 9;
        int i = 1;

        while (i <= 10) {

            System.out.println(number + " * " + " i " + " = " + (number * i));

            i++;


        }
    }
}

